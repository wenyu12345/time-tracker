"""岗位映射：关键字匹配 → 基础岗位

支持的关键字（按优先级排序）：
- 配料 → 配料
- 辊压 → 辊压
- 涂布 → 涂布
- 分条/激光切 → 激光切（两个关键字都匹配到激光切）
- 职能 → 职能
- 物料/物料员 → 物料（两个关键字都匹配到物料）
- 主管 → 主管
- 领班 → 领班
- 发片 → 发片

匹配规则：
1. 优先完全匹配
2. 然后按关键字包含匹配
3. 匹配到多个关键字时，按优先级选择第一个匹配的
"""

# 关键字优先级列表（前面的优先匹配）
KEYWORDS = [
    ('配料', '配料'),
    ('辊压', '辊压'),
    ('涂布', '涂布'),
    ('分条', '激光切'),    # 分条也匹配到激光切
    ('激光切', '激光切'),
    ('职能', '职能'),
    ('物料员', '物料'),     # 物料员优先于物料
    ('物料', '物料'),
    ('主管', '主管'),
    ('领班', '领班'),
    ('发片', '发片'),
]

# 细分岗位直接映射（精确匹配）
DETAIL_ROLE_MAPPING = {
    '正1D836涂布': '涂布', '正2D648涂布': '涂布', '正3A322涂布': '涂布',
    '负1D836涂布': '涂布', '负2D648涂布': '涂布', '负3A322涂布': '涂布',
    '正1A322辊压': '辊压', '正2D648辊压': '辊压',
    '负1D648/C951辊压': '辊压', '负2A322辊压': '辊压',
    '正极激光切': '激光切', '负极激光切': '激光切',
}

# 基础岗位列表（9个）
BASE_ROLES = ['配料', '涂布', '辊压', '激光切', '发片', '领班', '主管', '物料', '职能']


def get_base_role(role_name: str) -> str:
    """将输入岗位名映射到基础岗位
    
    匹配顺序：
    1. 精确匹配细分岗位（如"正1D836涂布" → "涂布"）
    2. 关键字包含匹配（如"激光切人员" → "激光切"）
    3. 返回原岗位名（不匹配时）
    
    Args:
        role_name: 输入的岗位名称
        
    Returns:
        基础岗位名称
    """
    if not role_name:
        return role_name
    
    # 1. 先尝试精确匹配细分岗位
    if role_name in DETAIL_ROLE_MAPPING:
        return DETAIL_ROLE_MAPPING[role_name]
    
    # 2. 去掉"人员"后缀再试精确匹配
    role_no_suffix = role_name.replace('人员', '').strip()
    if role_no_suffix in DETAIL_ROLE_MAPPING:
        return DETAIL_ROLE_MAPPING[role_no_suffix]
    
    # 3. 关键字匹配（按优先级）
    for keyword, base_role in KEYWORDS:
        if keyword in role_name:
            return base_role
    
    # 4. 返回原岗位名（不匹配时）
    return role_name
